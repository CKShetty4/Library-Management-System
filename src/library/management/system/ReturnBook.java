    public int calculateDuePrice(String dueDateStr, String returnDateStr) {
    SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
    try {
        Date dueDate = sdf.parse(dueDateStr);
        Date returnDate = sdf.parse(returnDateStr);

        if (returnDate.after(dueDate)) {
            long diffInMillis = returnDate.getTime() - dueDate.getTime();
            long diffInDays = diffInMillis / (1000 * 60 * 60 * 24); // Convert milliseconds to days
            return (int) diffInDays * 10; // â‚¹10 per day
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
    return 0; // No fine if returned on or before the due date
}

    

       
